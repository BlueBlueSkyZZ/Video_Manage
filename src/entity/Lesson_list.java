package entity;
	public class Lesson_list {
		private String teacher_id;
		private String lesson_id;
		private String lesson_name;
		private String lesson_info;
		private String lesson_data;
		private String lesson_type;
		public String getTeacher_id() {
			return teacher_id;
		}
		public void setTeacher_id(String teacher_id) {
			this.teacher_id = teacher_id;
		}
		public String getLesson_id() {
			return lesson_id;
		}
		public void setLesson_id(String lesson_id) {
			this.lesson_id = lesson_id;
		}
		public String getLesson_name() {
			return lesson_name;
		}
		public void setLesson_name(String lesson_name) {
			this.lesson_name = lesson_name;
		}
		public String getLesson_info() {
			return lesson_info;
		}
		public void setLesson_info(String lesson_info) {
			this.lesson_info = lesson_info;
		}
		public String getLesson_data() {
			return lesson_data;
		}
		public void setLesson_data(String lesson_data) {
			this.lesson_data = lesson_data;
		}
		public String getLesson_type() {
			return lesson_type;
		}
		public void setLesson_type(String lesson_type) {
			this.lesson_type = lesson_type;
		}
	}


